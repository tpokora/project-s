CREATE DATABASE users_database;

USE users_database;

CREATE TABLE USERS(
    ID INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT,
    USERNAME VARCHAR(32) NOT NULL,
    PASSWORD VARCHAR(32) NOT NULL,
    EMAIL VARCHAR(255) NOT NULL,
    ROLE VARCHAR(255) NOT NULL DEFAULT 'USER')
ENGINE=InnoDB;

